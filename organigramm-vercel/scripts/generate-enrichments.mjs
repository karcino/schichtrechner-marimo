#!/usr/bin/env node
// Generator für lib/enrichments.generated.ts.
//
// Läuft vor `next build` und `next dev`. Liest den immer vorhandenen Stub und
// — nur wenn BUILD_MODE=private — die optionale lokale enrichments.private.ts.
// Schreibt eine gebündelte Datei, von der enrichments.ts importiert.
//
// In public-Build: identisch zum Stub (leer).
// In private-Build mit existierender .private.ts: Stub + echte private Einträge.
//
// Sicherheit: die generierte Datei ist gitignored. Falls BUILD_MODE unerwartet
// auf public steht aber die .private.ts existiert, wird sie ignoriert — kein
// Leak-Risiko.
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const libDir = resolve(__dirname, "..", "lib");
const stubPath = resolve(libDir, "enrichments.private.stub.ts");
const privatePath = resolve(libDir, "enrichments.private.ts");
const outPath = resolve(libDir, "enrichments.generated.ts");

const buildMode = process.env.BUILD_MODE ?? "public";
const includePrivate = buildMode === "private" && existsSync(privatePath);

const banner = `// AUTO-GENERATED — do not edit by hand.
// Generated by scripts/generate-enrichments.mjs (BUILD_MODE=${buildMode}).
// Gitignored. Reruns on every npm run dev / build.
`;

const body = includePrivate
  ? `export { ENRICHMENTS_PRIVATE as STUB_PRIVATE } from "./enrichments.private.stub";
export { ENRICHMENTS_PRIVATE } from "./enrichments.private";
export const INCLUDES_PRIVATE = true as const;
`
  : `export { ENRICHMENTS_PRIVATE } from "./enrichments.private.stub";
export const INCLUDES_PRIVATE = false as const;
`;

writeFileSync(outPath, banner + "\n" + body);

// Sanity-Check: Stub-Datei muss existieren (committed fallback).
if (!existsSync(stubPath)) {
  console.error(`ERROR: Stub-Datei fehlt: ${stubPath}`);
  process.exit(1);
}

const status = includePrivate ? "private (stub + real)" : "public (stub only)";
console.log(`[enrichments-gen] Mode: ${status}`);
console.log(`[enrichments-gen] Wrote ${outPath}`);
